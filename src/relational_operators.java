import java.util.Scanner;

public class relational_operators {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
         System.out.println("Enter two numbers\n");
         int a=sc.nextInt();
         int b=sc.nextInt();
         System.out.println("a==b is "+(a==b));
         System.out.println("a!=b is "+(a!=b));
         System.out.println("a>b is "+(a>b));
         System.out.println("a<b is "+(a<b));
         System.out.println("a>=b is "+(a>=b));
         System.out.println("a<=b is "+(a<=b));
    }
    
}
